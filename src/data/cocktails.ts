export function formatMenu(): string {
  const alc = COCKTAILS.filter((c) => c.alcoholic);
  const soft = COCKTAILS.filter((c) => !c.alcoholic);
  const list = (items: Cocktail[]) =>
    items.map((c) => `  · ${c.name} — ${c.price}₽`).join("\n");
  return `МЕНЮ\nКрепкое:\n${list(alc)}\n\nБезалкогольное:\n${list(soft)}`;
}
